package BinaryTrees;

public class BT {

    String data;
    Integer numData;
    BT left;
    BT right;

    public String getData () {
        return data;
    }

    public void setData (String data) {
        this.data = data;
    }

    public BT getLeft () {
        return left;
    }

    public void setLeft (BT left) {
        this.left = left;
    }

    public BT getRight () {
        return right;
    }

    public void setRight (BT right) {
        this.right = right;
    }

    public Integer getNumData () {
        return numData;
    }

    public void setNumData (Integer numData) {
        this.numData = numData;
    }
}
